
import math

print(abs(-2.5))   # gives the positive value

print(math.ceil(2.333))  # give the smallest integer not less than x

print(math.exp(3))  # gives e^3

print(math.fabs(-2.55))  # it also gives absolute value

print(math.floor(2.333))  # gives the largest integer not greater than x

print(math.log(2))  # give the natural logarithm log2

print(math.log10(100))  # gives the log with base 10

print(max(10, 20, -34, 11, 14))     # gives greatest of the sequence

print(min(22, 12, 18, -23, 56))     # gives smallest of the sequence

print(math.modf(22.43343))  # seperate int and float part and returns tuple

print(pow(2, 3))     # x**y

print(round(22.334, 2))  #roundoff the number by the given decimal point

print(math.factorial(5))    # returns factorial of given number

print(math.trunc(2.333))    # returns truncated value

# trignometry function

print(math.cos(math.pi/3))    #r eturns cosine of radians

print(math.sin(math.pi/6))  # returns sine of radians

print(math.tan(math.pi/4))  # returns tan of radians

print(math.acos(1/2))    # returns arc cosine of radians

print(math.asin(1/2))  # returns arc sine of radians

print(math.atan(1))  # returns arc tan of radians

print(math.degrees(math.pi/3))  # converts radian into degree

print(math.radians(60))  # converts degree into radians

print(math.hypot(4, 3))  # returns hypotenuse of x,y



 
